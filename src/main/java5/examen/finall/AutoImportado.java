package examen.finall;

public class AutoImportado extends Auto {

	private String codigoImportacion;
	private String valorImportacion;
	
	//METODOS SET Y GET
	public String getCodigoImportacion() {
		return codigoImportacion;
	}
	public void setCodigoImportacion(String codigoImportacion) {
		this.codigoImportacion = codigoImportacion;
	}
	public String getValorImportacion() {
		return valorImportacion;
	}
	public void setValorImportacion(String valorImportacion) {
		this.valorImportacion = valorImportacion;
	}
}
